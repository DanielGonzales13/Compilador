    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            if(archivoseleccionado != ""){
                File archivo = new File(archivoseleccionado);
                BufferedWriter escritor = new BufferedWriter(new FileWriter(archivo));
                escritor.write(jTextArea2.getText());
                escritor.close();
                Reader read = new BufferedReader(new FileReader(archivo));
                Lexer lexer = new Lexer(read);
                String resultado = "";
                DefaultTableModel model = new DefaultTableModel();
                ArrayList<Object> columnas = new ArrayList<Object>();
                columnas.add("LEXEMA");
                columnas.add("TOKEN");
                columnas.add("LINEA");
                for(Object col: columnas){
                    model.addColumn(col);
                }
                this.jTable1.setModel(model);

                jTable1.setDefaultRenderer(Object.class, new CustomTableCellRenderer());

                int linea_actual = 1;
                while (true) {
                    Tokens token = lexer.yylex();
                    if (token == null){
                        resultado = resultado+"EOF";

                        return;
                    }
                    switch(token){
                        case linea:
                        linea_actual++;
                        break;
                        default:
                        resultado=resultado+"TOKEN: " + token + " LEXEMA: " + lexer.lexeme + "\n";
                        Object[] info1 = new Object[]{lexer.lexeme, token, linea_actual};
                        model.addRow(info1);
                    }
                }
            }
            else{
                JOptionPane.showMessageDialog(this, "Seleccione un archivo", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(FrmPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(FrmPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        int returnValue = fileChooser.showOpenDialog(null);
        if (returnValue == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();
            archivoseleccionado = selectedFile.toString();
            try (BufferedReader br = new BufferedReader(new FileReader(selectedFile))) {
                String line;
                StringBuilder content = new StringBuilder();
                while ((line = br.readLine()) != null) {
                    content.append(line).append("\n");
                }
                jTextArea2.setText(content.toString());
            } catch (IOException e) {
                JOptionPane.showMessageDialog(this, "Error al leer el archivo", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(archivoseleccionado != ""){
            BufferedWriter escritor = null;
            try {
                File archivo = new File(archivoseleccionado);
                escritor = new BufferedWriter(new FileWriter(archivo));
                escritor.write(jTextArea2.getText());
                escritor.close();
                String codigo = jTextArea2.getText();
                Sintax s = new Sintax(new codigo.LexerCup(new StringReader(codigo)));
                jTextArea3.setText("");
                
                try {
                    s.parse();
                    jTextArea1.setText("Analisis Correcto");
                    jTextArea1.setForeground(Color.black);

                } catch (Exception ex) {
                    Symbol sym = s.getS();
                    jTextArea1.setText("Error de sintaxis, linea: " + (sym.right+1) + " Columna: " + (sym.left+1) + " Texto: " + sym.value);
                    jTextArea1.setForeground(Color.red);
                }
            } catch (IOException ex) {
                Logger.getLogger(FrmPrincipal.class.getName()).log(Level.SEVERE, null, ex);
            } finally {
                try {
                    escritor.close();
                } catch (IOException ex) {
                    Logger.getLogger(FrmPrincipal.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
        else{
            JOptionPane.showMessageDialog(this, "Seleccione un archivo", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

